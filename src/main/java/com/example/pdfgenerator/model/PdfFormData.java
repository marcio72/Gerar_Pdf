package com.example.pdfgenerator.model;

public class PdfFormData {
    private String nomePonto;
    private String endereco;
    private String leiturista;
    private String contato;
    private String telefone;
    private String maquina;
    private String jogo;
    private String e;
    private String s;
    private String placa;

    // Getters e Setters
    public String getNomePonto() { return nomePonto; }
    public void setNomePonto(String nomePonto) { this.nomePonto = nomePonto; }

    public String getEndereco() { return endereco; }
    public void setEndereco(String endereco) { this.endereco = endereco; }

    public String getLeiturista() { return leiturista; }
    public void setLeiturista(String leiturista) { this.leiturista = leiturista; }

    public String getContato() { return contato; }
    public void setContato(String contato) { this.contato = contato; }

    public String getTelefone() { return telefone; }
    public void setTelefone(String telefone) { this.telefone = telefone; }

    public String getMaquina() { return maquina; }
    public void setMaquina(String maquina) { this.maquina = maquina; }
    
    public String getJogo() { return jogo; }
    public void setJogo(String jogo) { this.jogo = jogo; }


    public String getE() { return e; }
    public void setE(String e) { this.e = e; }

    public String getS() { return s; }
    public void setS(String s) { this.s = s; }

    public String getPlaca() { return placa; }
    public void setPlaca(String placa) { this.placa = placa; }
}
